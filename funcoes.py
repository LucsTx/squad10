def soma (num1, num2):
    somatoria = num1 + num2
    return print('O resultado da soma e', somatoria) 

def subtracao (num1, num2):
    subtrai = num1 - num2
    return print ('O resultado da subtracao e', subtrai)

def multiplicacao (num1, num2):
    multiplica = num1 * num2
    return print ('O resultado da multiplicacao e', multiplica)

def divisao (num1, num2):
    divide = num1 / num2
    return print ('O resultado da divisao e {:.2f}'.format (divide))

def resDivisao (num1, num2):
    resto = num1 % num2 
    print ('O resto da divisao e', resto) 

def divideInt (num1, num2):
    resultInt = num1 // num2
    print ('O resultado inteiro da divisao e', resultInt)

